
package exerciciomath;

public class MultiplosDeDez {
    public static void main(String[] args) {
        
        for(int i = 0; i < 100; i++){
            if(i % 10 == 0){
                System.out.println("\nMultiplo de 10: " + 1);
            }else {
                System.out.println("" + i + ", "  );
            }
        }
    }
}
